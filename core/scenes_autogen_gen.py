        label = str(s.get("label") or "").strip() or "?"
        sid = str(s.get("id") or "").strip()
        safe = _safe_ident(label or sid or "scene")
        scene_h = export_dir / f"scene_{safe}.h"
        level_h = export_dir / f"scene_{safe}_level.h"
